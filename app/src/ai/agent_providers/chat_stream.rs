/// Issue #94 regression test: when `build_chat_request` collects messages from multiple tasks, history-turn user messages
/// must not be reordered to the end, nor duplicated due to LRC subagent copies.
#[cfg(test)]
mod issue_94_task_linearization_tests {
    use super::*;
    use crate::test_util::ai_agent_tasks::{
        create_api_subtask, create_api_task, create_message, create_subagent_tool_call_message,
    };

    /// Build a UserQuery message with a `request_id`.
    fn user_query_msg(id: &str, task_id: &str, request_id: &str, query: &str) -> api::Message {
        api::Message {
            id: id.to_string(),
            task_id: task_id.to_string(),
            server_message_data: String::new(),
            citations: vec![],
            message: Some(api::message::Message::UserQuery(api::message::UserQuery {
                query: query.to_string(),
                ..Default::default()
            })),
            request_id: request_id.to_string(),
            timestamp: None,
        }
    }

    /// Reproduce Issue #94's 01.json / 02.json scenario: the user only sent one sentence, but after the LRC CLI
    /// subagent forks, this UserQuery exists in both the root task and the subtask.
    ///
    /// - root: [UserQuery, AgentOutput, Subagent ToolCall→subtask]
    /// - subtask: [UserQuery(a copy with the same request_id+query), AgentOutput]
    fn issue_94_tasks() -> (api::Task, api::Task) {
        const REQ: &str = "req-1";
        const QUERY: &str = "help me set up a dns (doh) split-tunneling service on this server";
        let root = create_api_task(
            "root",
            vec![
                user_query_msg("m1", "root", REQ, QUERY),
                create_message("m2", "root"),
                create_subagent_tool_call_message("m3", "root", "sub-1", None),
            ],
        );
        let subtask = create_api_subtask(
            "sub-1",
            "root",
            vec![
                user_query_msg("s1", "sub-1", REQ, QUERY),
                create_message("s2", "sub-1"),
            ],
        );
        (root, subtask)
    }

    fn count_user_queries(msgs: &[&api::Message]) -> usize {
        msgs.iter()
            .filter(|m| matches!(&m.message, Some(api::message::Message::UserQuery(_))))
            .count()
    }

    fn message_ids(msgs: &[&api::Message]) -> Vec<String> {
        msgs.iter().map(|m| m.id.clone()).collect()
    }

    /// Reproduction: the old implementation's naive concatenation `params.tasks.iter().flat_map(|t| t.messages.iter())` ——
    /// (1) UserQuery appears twice; (2) the result varies with task input order (`compute_active_tasks`
    /// collects via HashMap::into_values, whose order is non-deterministic).
    #[test]
    fn naive_flat_map_reproduces_issue_94() {
        let (root, subtask) = issue_94_tasks();

        let naive = |tasks: &[api::Task]| -> Vec<String> {
            tasks
                .iter()
                .flat_map(|t| t.messages.iter())
                .map(|m| m.id.clone())
                .collect()
        };

        let root_first = naive(&[root.clone(), subtask.clone()]);
        let subtask_first = naive(&[subtask.clone(), root.clone()]);

        // (1) UserQuery is concatenated twice.
        let root_first_refs: Vec<&api::Message> = [&root, &subtask]
            .iter()
            .flat_map(|t| t.messages.iter())
            .collect();
        assert_eq!(
            count_user_queries(&root_first_refs),
            2,
            "naive concatenation makes the same user query appear twice —— this is exactly the Issue #94 bug"
        );

        // (2) Order drifts with input task order —— when subtask comes first, the history user (m1) gets thrown to the end.
        assert_ne!(
            root_first, subtask_first,
            "naive concatenation result depends on task order, non-deterministic"
        );
        assert_eq!(
            subtask_first.last().map(String::as_str),
            Some("m3"),
            "when subtask comes first, root's messages all shift to the back"
        );
        assert!(
            subtask_first.iter().position(|id| id == "s1").unwrap()
                < subtask_first.iter().position(|id| id == "m1").unwrap(),
            "the subtask's UserQuery copy (s1) got ordered before the root original (m1)"
        );
    }

    /// Fix verification: `collect_linearized_task_messages` output is independent of task input order,
    /// UserQuery is deduplicated down to one, and the whole is a root→subtask DFS linear order.
    #[test]
    fn linearized_collection_is_deterministic_and_deduped() {
        let (root, subtask) = issue_94_tasks();

        let root_first = vec![root.clone(), subtask.clone()];
        let subtask_first = vec![subtask.clone(), root.clone()];

        let a = collect_linearized_task_messages(&root_first);
        let b = collect_linearized_task_messages(&subtask_first);

        // Independent of input order.
        assert_eq!(
            message_ids(&a),
            message_ids(&b),
            "the result must be independent of the input order of params.tasks"
        );

        // UserQuery dedup: the subtask copy (s1) produced by LRC is dropped.
        assert_eq!(
            count_user_queries(&a),
            1,
            "duplicate UserQuery must be deduplicated to one"
        );

        // DFS linear order: root's messages come first, descending into the subtask upon a Subagent ToolCall.
        // s1 is deduplicated, so [m1, m2, m3, s2] is expected.
        assert_eq!(message_ids(&a), vec!["m1", "m2", "m3", "s2"]);

        // The retained user query is the root original, placed at the start of the sequence.
        assert_eq!(a.first().map(|m| m.id.as_str()), Some("m1"));
    }

    /// A normal single-task conversation (no subagent) is unaffected: messages are returned as-is, in original order.
    #[test]
    fn single_task_conversation_unchanged() {
        let root = create_api_task(
            "root",
            vec![
                user_query_msg("m1", "root", "req-1", "hello"),
                create_message("m2", "root"),
            ],
        );
        let out = collect_linearized_task_messages(std::slice::from_ref(&root));
        assert_eq!(message_ids(&out), vec!["m1", "m2"]);
    }

    /// Even if different user turns have the same query text, they are not mistakenly deleted as long as the `request_id` differs.
    #[test]
    fn distinct_turns_with_same_text_are_kept() {
        let root = create_api_task(
            "root",
            vec![
                user_query_msg("m1", "root", "req-1", "continue"),
                create_message("m2", "root"),
                user_query_msg("m3", "root", "req-2", "continue"),
            ],
        );
        let out = collect_linearized_task_messages(std::slice::from_ref(&root));
        assert_eq!(
            count_user_queries(&out),
            2,
            "both user messages from two turns with different request_id must be kept"
        );
        assert_eq!(message_ids(&out), vec!["m1", "m2", "m3"]);
    }
}
