/// Whether this turn's input contains `UserQueryMode::Plan` triggered by `/plan`.
///
/// Per-turn semantics: only looks at whether this turn's `params.input` carries the Plan marker. The current persistence path
/// for historical task messages (`make_user_query_message`) writes the upstream proto with `..Default::default()`,
/// **without the mode field**; so plan state doesn't automatically stick across turns, and the user must re-add
/// the `/plan ` prefix to every query they want to keep read-only. This is a deliberate MVP form:
/// - lowest implementation cost (no need to change the proto schema, no new session-level state machine)
/// - consistent with the "explicit enter/exit" semantics of Claude Code `EnterPlanMode` —— except here the
///   exit action is implied by "the next query without /plan"
fn is_plan_mode_turn(input: &[AIAgentInput]) -> bool {
    input.iter().any(|i| {
        matches!(
            i,
            AIAgentInput::UserQuery {
                user_query_mode: UserQueryMode::Plan,
                ..
            }
        )
    })
}

/// Write/execution-type built-in tool names hard-filtered under Plan Mode.
///
/// A logical fallback: even if the model ignores the guidance in `partials/plan_mode.j2`, it can't trigger side effects ——
/// a tool not in the tool list can't be called (the provider protocol layer directly rejects an unknown function).
///
/// **Write-type tools NOT blocked**: `create_documents` / `edit_documents`. They only touch
/// Zap Drive's local document storage (AIDocumentModel), don't touch the filesystem, and don't run commands; semantically
/// they happen to be the output-archiving action of Plan Mode —— the model captures the final plan as a Drive document,
/// which the user can later view / edit / drag into a self-built PLAN folder in the Drive UI for reuse.
///
/// The read-only + Drive-write subset left behind: `read_files / grep / file_glob_v2 /
/// read_shell_command_output / ask_user_question / read_skill / read_documents /
/// create_documents / edit_documents / webfetch / websearch / mcp/*`.
const PLAN_MODE_BLOCKED_TOOLS: &[&str] = &[
    "run_shell_command",
    "apply_file_diffs",
    "write_to_long_running_shell_command",
    "open_code_review",
    "transfer_shell_command_control_to_user",
    "suggest_prompt",
];

/// Lists the tool names actually fed to the upstream model this turn (built-in REGISTRY + current MCP tools),
/// sharing the same gating as `build_tools_array` (LRC / `web_search_enabled` /
/// `suggest_new_conversation` / `plan_mode`). For `prompt_renderer` to inject into the
/// system prompt, so the template renders dynamically from the actually-available list rather than hardcoding an allow/block list.
pub fn available_tool_names(params: &RequestParams) -> Vec<String> {
    let is_lrc = params.lrc_command_id.is_some();
    let web_enabled = params.web_search_enabled;
    let plan_mode = is_plan_mode_turn(&params.input);
    let mut names: Vec<String> = tools::REGISTRY
        .iter()
        .filter(|t| {
            if is_lrc && t.name == "run_shell_command" {
                return false;
            }
            if !web_enabled
                && (t.name == tools::webfetch::TOOL_NAME || t.name == tools::websearch::TOOL_NAME)
            {
                return false;
            }
            if t.name == "suggest_new_conversation" {
                return false;
            }
            if plan_mode && PLAN_MODE_BLOCKED_TOOLS.contains(&t.name) {
                return false;
            }
            true
        })
        .map(|t| t.name.to_owned())
        .collect();
    if let Some(ctx) = params.mcp_context.as_ref() {
        for (name, _description, _parameters) in tools::mcp::build_mcp_tool_defs(ctx) {
            names.push(name);
        }
    }
    names
}

fn build_tools_array(params: &RequestParams) -> Vec<GenaiTool> {
    // Zap A2: in the LRC tag-in scenario, remove `run_shell_command` to force the model to choose a PTY-operation tool.
    //
    // Under an alt-screen long command (nvim/htop) + user tag-in state, **the mistake the model is most prone to** is
    // calling `run_shell_command` to run `taskkill nvim` / `Stop-Process nvim` (spawning a new process),
    // which has nothing to do with the currently running PTY and won't kill the target. **The correct approach** is
    // `write_to_long_running_shell_command(command_id, input=":q\n", mode=raw)`,
    // sending the command directly to the current PTY.
    //
    // In practice, system prompt guidance + the RunningCommand context prefix are both not strong enough,
    // and the model still leans toward run_shell_command (it's simpler). The cleanest hard constraint is to remove the tool
    // directly from the tools list, so the model can only choose among the PTY-operation tools.
    //
    // Other tools are kept (read_files/grep/ask_user_question, etc.), allowing the model to do the necessary
    // information gathering and asking back.
    let is_lrc = params.lrc_command_id.is_some();
    let web_enabled = params.web_search_enabled;
    let plan_mode = is_plan_mode_turn(&params.input);
    // Zap BYOP: the `suggest_prompt` chip UI is restored via the view layer subscribing to
    // PromptSuggestionExecutorEvent (see `terminal/view.rs::
    // handle_suggest_prompt_executor_event`), so it can be exposed to the model.
    // `suggest_new_conversation` is still filtered: the UX has no ready-made dialog component, and the executor has been changed to
    // fast-fail Cancelled (see `action_model/execute/suggest_new_conversation.rs`),
    // so the filter is redundant defense to avoid invalid-call noise.
    // Dynamic placeholder replacement: some tool descriptions contain `{{year}}` (such as websearch, aligned with opencode
    // websearch.ts:30-32's description getter), replaced with the current year at build time.
    // The description the model sees always has the correct year and won't be polluted by an old year from training data.
    let current_year = chrono::Local::now().format("%Y").to_string();
    let mut out: Vec<GenaiTool> = tools::REGISTRY
        .iter()
        .filter(|t| {
            if is_lrc && t.name == "run_shell_command" {
                return false;
            }
            // BYOP web tools are gated by profile.web_search_enabled (not exposed to the upstream model when the user has turned off the privacy
            // toggle, to avoid mistakenly invoking external network requests).
            if !web_enabled
                && (t.name == tools::webfetch::TOOL_NAME || t.name == tools::websearch::TOOL_NAME)
            {
                return false;
            }
            // suggest_new_conversation: no UI implementation; the executor in Zap is changed to
            // fast-fail Cancelled. We filter it out here to avoid the model's call producing a meaningless
            // tool_call→cancelled round trip (pure token waste).
            if t.name == "suggest_new_conversation" {
                return false;
            }
            // Plan Mode: a hard guardrail for the read-only mode triggered by `/plan`, removing write/execution-type tools.
            // Double insurance with the system prompt's plan_mode.j2 guidance —— even if the model ignores
            // the prompt, a tool not in the list can't trigger side effects (the provider protocol layer
            // directly rejects an unknown function).
            if plan_mode && PLAN_MODE_BLOCKED_TOOLS.contains(&t.name) {
                return false;
            }
            true
        })
        .map(|t| {
            let description = if t.description.contains("{{year}}") {
                t.description.replace("{{year}}", &current_year)
            } else {
                t.description.to_owned()
            };
            GenaiTool::new(t.name)
                .with_description(description)
                .with_schema((t.parameters)())
        })
        .collect();

    if let Some(ctx) = params.mcp_context.as_ref() {
        for (name, description, parameters) in tools::mcp::build_mcp_tool_defs(ctx) {
            out.push(
                GenaiTool::new(name)
                    .with_description(description)
                    .with_schema(parameters),
            );
        }
    }
    if is_lrc {
        log::info!(
            "[byop] LRC tag-in: tools array filtered (removed run_shell_command), \
             total tools={}",
            out.len()
        );
    }
    if plan_mode {
        log::info!(
            "[byop] Plan Mode: tools array filtered (removed write/exec tools: {:?}), \
             total tools={}",
            PLAN_MODE_BLOCKED_TOOLS,
            out.len()
        );
    }
    out
}

