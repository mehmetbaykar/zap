//! The chat completion + tool calling adaptation layer for BYOP mode (based on genai 0.5.3).
//!
//! Translates `RequestParams` into a genai `ChatRequest`, calls the user-configured provider via `Client::exec_chat_stream`,
//! translates the response back into `warp_multi_agent_api::ResponseEvent`, and
//! the controller's own logic (permissions/dialogs/execution/result write-back/triggering the next turn) takes over the loop.
//!
//! ## Explicit routing of the 5 API protocols
//!
//! Instead of force-fitting all providers as OpenAI-compatible, a `ServiceTargetResolver`
//! maps the `AgentProviderApiType` the user selects in the settings UI one-to-one to genai's `AdapterKind`:
//!
//! | ApiType        | AdapterKind  | default endpoint                               |
//! |----------------|--------------|------------------------------------------------|
//! | OpenAi         | OpenAI       | https://api.openai.com/v1                      |
//! | OpenAiResp     | OpenAIResp   | https://api.openai.com/v1 (uses /v1/responses) |
//! | Gemini         | Gemini       | https://generativelanguage.googleapis.com/v1beta |
//! | Anthropic      | Anthropic    | https://api.anthropic.com                      |
//! | Ollama         | Ollama       | http://localhost:11434                         |
//!
//! The user-entered `base_url` always overrides the default. This way:
//! - OpenAI-compatible providers like DeepSeek / SiliconFlow / OpenRouter select `OpenAi` with a custom base_url
//! - explicitly choosing the adapter completely bypasses genai's "identify by model name" default behavior, avoiding misidentification
//!
//! ## Multi-turn message conversion
//!
//! - system prompt: `ChatRequest::with_system()` (not in the messages array)
//! - user query: `ChatMessage::user(text)`
//! - assistant text: `ChatMessage::assistant(text)`
//! - assistant tool_calls: `ChatMessage::from(Vec<ToolCall>)` (automatically assistant role)
//! - tool result: `ChatMessage::from(ToolResponse::new(call_id, content))` (automatically tool role)
//!
//! ## Streaming implementation
//!
//! `Client::exec_chat_stream` returns a `ChatStreamResponse`, whose `stream` field implements
//! `futures_core::Stream<Item = Result<ChatStreamEvent>>`. Events:
//! - `Start` / `Chunk(text)` / `ReasoningChunk(text)` / `ToolCallChunk(tool_call)` / `End(StreamEnd)`
//!
//! We immediately emit `AppendToMessageContent` for Chunk/ReasoningChunk (typewriter effect),
//! accumulate a buffer for ToolCallChunk (by call_id), uniformly emit `Message::ToolCall` at the end of the stream,
//! and the controller automatically takes over.

