/// Maps `AgentProviderApiType` one-to-one to a genai `AdapterKind`.
fn adapter_kind_for(api_type: AgentProviderApiType) -> AdapterKind {
    match api_type {
        AgentProviderApiType::OpenAi => AdapterKind::OpenAI,
        AgentProviderApiType::OpenAiResp => AdapterKind::OpenAIResp,
        AgentProviderApiType::Gemini => AdapterKind::Gemini,
        AgentProviderApiType::Anthropic => AdapterKind::Anthropic,
        AgentProviderApiType::Ollama => AdapterKind::Ollama,
        AgentProviderApiType::DeepSeek => AdapterKind::DeepSeek,
    }
}

/// Normalizes the user-entered `base_url`, producing the endpoint URL for the genai adapter to concatenate the service path onto.
///
/// All genai 0.6.x adapters assume the endpoint ends with `/` and already contains the version path segment:
/// - Anthropic: `format!("{base_url}messages")` expects `…/v1/`
/// - Gemini: `format!("{base_url}models/{m}:streamGenerateContent")` expects `…/v1beta/`
/// - OpenAI / OpenAIResp / DeepSeek: `Url::join("chat/completions" or "responses")` expects `…/v1/`
/// - Ollama: `format!("{base_url}api/chat")` expects the root path `…/`
///
/// The user actually enters in three ways:
/// 1. bare host (`https://ai.zerx.dev`) — the early default behavior of only appending a trailing `/` would concatenate into `https://ai.zerx.dev/messages`
///    missing `/v1/` and causing a 404. **Here we automatically append the default version path segment per api_type** (Anthropic/OpenAI family→`/v1/`,
///    Gemini→`/v1beta/`, Ollama doesn't append).
/// 2. full with version path (`https://ai.zerx.dev/v1`) — only append a trailing `/`, leaving the path untouched.
/// 3. left empty — use [`AgentProviderApiType::default_base_url`].
fn normalize_endpoint_url(api_type: AgentProviderApiType, base_url: &str) -> String {
    let trimmed = base_url.trim();
    if trimmed.is_empty() {
        return api_type.default_base_url().to_owned();
    }

    // Parse failure (the user entered a malformed URL) → degrade to the original "append trailing /" behavior, letting the upstream error rather than panicking here.
    let parsed = match url::Url::parse(trimmed) {
        Ok(u) => u,
        Err(_) => {
            let stripped = trimmed.trim_end_matches('/');
            return format!("{stripped}/");
        }
    };

    // path == "/" or empty → the user entered only the host, so automatically append the api_type default version path segment.
    if parsed.path() == "/" || parsed.path().is_empty() {
        // Extract the path part from default_base_url (such as "/v1/" / "/v1beta/" / "/").
        let default_path = url::Url::parse(api_type.default_base_url())
            .ok()
            .map(|u| u.path().to_owned())
            .unwrap_or_else(|| "/".to_owned());
        let host_part = trimmed.trim_end_matches('/');
        return format!("{host_part}{default_path}");
    }

    // The user already brought a path → only ensure a trailing `/` (genai format!/Url::join both depend on it).
    let stripped = trimmed.trim_end_matches('/');
    format!("{stripped}/")
}

/// Builds a genai Client. Created fresh per request (low cost — internally a Client is just reqwest::Client + adapter table).
/// After `ServiceTargetResolver` captures the current request's endpoint/key/api_type, it force-routes every exec_chat_stream
/// to the specified AdapterKind, completely bypassing genai's default "identify by model name".
pub(super) fn build_client(
    api_type: AgentProviderApiType,
    base_url: String,
    api_key: String,
) -> Client {
    let adapter_kind = adapter_kind_for(api_type);
    let endpoint_url = normalize_endpoint_url(api_type, &base_url);
    log::info!("[byop] build_client: adapter={adapter_kind:?} endpoint_url={endpoint_url}");
    let key_for_resolver = api_key.clone();
    let resolver = ServiceTargetResolver::from_resolver_fn(
        move |service_target: ServiceTarget| -> Result<ServiceTarget, genai::resolver::Error> {
            let ServiceTarget { model, .. } = service_target;
            let endpoint = Endpoint::from_owned(endpoint_url.clone());
            let auth = AuthData::from_single(key_for_resolver.clone());
            // Override genai's "by model name" identification result with our specified AdapterKind,
            // but keep model_name so the upstream service addresses the model correctly.
            let model = ModelIden::new(adapter_kind, model.model_name);
            Ok(ServiceTarget {
                endpoint,
                auth,
                model,
            })
        },
    );

    // Zap BYOP: the SSE stream must not carry gzip. `Accept-Encoding: gzip` makes nginx-style
    // proxies compress the response, and the server must flush a complete deflate frame before the client can decode
    // the plaintext, breaking the streaming semantics into ~K-byte bursts that feel like "a stutter every few hundred milliseconds". zed/opencode
    // use native fetch / std HTTP and don't proactively negotiate gzip on SSE, so they have no problem with the same proxy.
    //
    // We explicitly construct `WebConfig` here even though the genai default is already `gzip=false` (a fork modification).
    //
    // The User-Agent is dynamically bound to the current application name (taken from `ChannelState::app_id().application_name()`,
    // registered by the entry bin: `bin/oss.rs` → "Zap"; other channels carry their own names).
    // This lets the upstream service identify which fork build the request comes from, and it automatically follows any later rename.
    let mut headers = reqwest::header::HeaderMap::new();
    if let Ok(value) = build_user_agent_header() {
        headers.insert(reqwest::header::USER_AGENT, value);
    }
    let web_config = WebConfig {
        gzip: false,
        default_headers: Some(headers),
        ..WebConfig::default()
    };
    Client::builder()
        .with_web_config(web_config)
        .with_service_target_resolver(resolver)
        .build()
}

/// Builds the `User-Agent` header for BYOP outbound requests, with a value like:
/// - `Zap/<git-tag>` —— for release builds with `GIT_RELEASE_TAG` injected
/// - `Zap` —— for Dev / local builds with no version
///
/// The application name is always taken from `ChannelState::app_id().application_name()`, ensuring consistency with the `AppId`
/// the entry bin registers (`bin/oss.rs` registers "Zap").
fn build_user_agent_header(
) -> Result<reqwest::header::HeaderValue, reqwest::header::InvalidHeaderValue> {
    let app_name = warp_core::channel::ChannelState::app_id()
        .application_name()
        .to_owned();
    let ua = match warp_core::channel::ChannelState::app_version() {
        Some(v) if !v.is_empty() => format!("{app_name}/{v}"),
        _ => app_name,
    };
    reqwest::header::HeaderValue::from_str(&ua)
}

/// Determines whether to inject `enable_thinking: true` for DashScope (Alibaba Cloud Bailian, OpenAI-compatible path).
///
/// Aligned with opencode `transform.ts:931-938` (the comment at provider/transform.ts L926+):
/// "DashScope doesn't enable thinking by default; reasoning models like qwen3 / qwq / deepseek-r1 / kimi-k2.5 / qwen-plus
/// must explicitly set `enable_thinking: true` to output reasoning_content".
///
/// Match conditions (all must hold):
/// 1. `api_type == OpenAi` (DashScope uses the OpenAI-compatible path)
/// 2. `effort_setting != Off` (respect the user actively turning off thinking; don't inject)
/// 3. base_url contains `dashscope.aliyuncs.com` / `dashscope.cn` / `dashscope-intl.aliyuncs.com`
/// 4. model_id does not contain `kimi-k2-thinking` (opencode excludes it, since that model thinks by default)
/// 5. model_id matches the reasoning substring allowlist: `qwen3` / `qwq` / `deepseek-r1` / `kimi-k2.5` /
///    `kimi-k2-` / `qwen-plus` (to avoid stuffing it into pure chat models like qwen-turbo / qwen2.5)
fn dashscope_needs_enable_thinking(
    api_type: AgentProviderApiType,
    base_url: &str,
    model_id: &str,
    effort_setting: crate::settings::ReasoningEffortSetting,
) -> bool {
    if !matches!(api_type, AgentProviderApiType::OpenAi) {
        return false;
    }
    if matches!(effort_setting, crate::settings::ReasoningEffortSetting::Off) {
        return false;
    }
    let url = base_url.to_ascii_lowercase();
    let is_dashscope = url.contains("dashscope.aliyuncs.com")
        || url.contains("dashscope.cn")
        || url.contains("dashscope-intl.aliyuncs.com");
    if !is_dashscope {
        return false;
    }
    let id = model_id.to_ascii_lowercase();
    if id.contains("kimi-k2-thinking") {
        return false;
    }
    id.contains("qwen3")
        || id.contains("qwq")
        || id.contains("deepseek-r1")
        || id.contains("kimi-k2.5")
        || id.contains("kimi-k2-")
        || id.contains("qwen-plus")
}

/// Infers the upstream provider from base_url, **only** to decide whether to send `prompt_cache_key`.
///
/// Aligned with the `options()` function in opencode `packages/opencode/src/provider/transform.ts`:
/// opencode uses the `providerID` dimension to decide whether to set
/// `promptCacheKey`, and only the five providers `openai` / `azure` / `openrouter` / `venice` /
/// `opencode*` send it. All other providers (including all OpenAI-compatible
/// relays / local services / most domestic clouds) never send it.
///
/// On the Zap side there's no `providerID` dimension, only the user-entered `base_url`.
/// So we infer from `base_url`:
/// - `api.openai.com`           → "openai"
/// - `*.openai.azure.com`       → "azure"
/// - `openrouter.ai/api`        → "openrouter"
/// - `api.venice.ai/api`        → "venice"
/// - `opencode.ai/zen`          → "opencode"
///
/// Everything else returns `None` (equivalent to not matching any branch in opencode).
///
/// Data source: the `api` field of the provider table in [models.dev](https://models.dev/api.json).
/// openai / azure / venice have no `api` in models.dev (baked into the SDK),
/// so they're hardcoded here per each SDK's default endpoint.
///
/// Deliberately covering only these 5 \u{2014} this is opencode's real allowlist; don't expand it because "there seem to be
/// other cache-compatible providers". OpenRouter's docs natively support
/// `prompt_cache_key` (snake_case); the other four use the OpenAI Chat Completions path.
fn opencode_compatible_cache_provider(base_url: &str) -> bool {
    let u = base_url.to_ascii_lowercase();
    u.contains("api.openai.com")
        || u.contains(".openai.azure.com")
        || u.contains("openrouter.ai/api")
        || u.contains("api.venice.ai/api")
        || u.contains("opencode.ai/zen")
}

fn build_chat_options(
    api_type: AgentProviderApiType,
    base_url: &str,
    model_id: &str,
    effort_setting: crate::settings::ReasoningEffortSetting,
    extra_headers: Vec<(String, String)>,
    conversation_id: Option<&str>,
) -> ChatOptions {
    let mut opts = ChatOptions::default()
        .with_capture_content(true)
        .with_capture_tool_calls(true)
        .with_capture_reasoning_content(true)
        .with_capture_usage(true)
        // Have genai extract the <think>...</think> segments that DeepSeek-style models embed in content
        // and route them to the reasoning chunk, for a cleaner UI display. Only takes effect for adapters that support this format.
        .with_normalize_reasoning_content(true);

    // Prompt caching (1:1 aligned with the `options()` function in opencode `packages/opencode/src/provider/transform.ts`).
    // Key points:
    //
    // 1. **The `prompt_cache_retention` field (genai `ChatOptions::cache_control`) is never sent**.
    //    The entire opencode repo doesn't use this field; strict-schema-validating BYOP relays
    //    (OpenCode Go / vLLM / lm-studio / most domestic proxies) reject it with HTTP 400
    //    `Extra inputs are not permitted, field: 'prompt_cache_retention'`
    //    (issue #126). Even official OpenAI has a 5min implicit cache by default, with no need to declare it explicitly.
    //
    // 2. **`prompt_cache_key` is only sent for the 5 providers opencode knows**:
    //    `openai` / `azure` / `openrouter` / `venice` / `opencode`. All other providers
    //    (including all OpenAI-compatible relays / domestic clouds / local services) never send it.
    //
    //    opencode judges by `providerID` (the string the user selects in config); the Zap side has no
    //    `providerID`, so it can only infer from `base_url` → see `opencode_compatible_cache_provider`.
    //    This is base_url's only semantic use; don't extend it to decide behavior beyond caching.
    //
    // 3. Anthropic uses per-message cache_control (in `build_chat_request`),
    //    not here.
    // 4. DeepSeek / Gemini / Ollama cache implicitly server-side, so skip.
    if matches!(
        api_type,
        AgentProviderApiType::OpenAi | AgentProviderApiType::OpenAiResp
    ) && opencode_compatible_cache_provider(base_url)
    {
        if let Some(cid) = conversation_id {
            if !cid.is_empty() {
                opts = opts.with_prompt_cache_key(cid.to_owned());
            }
        }
    }

    // **Thinking-depth level dispatch** (aligned with how Zed `LanguageModelRequest::thinking_allowed` handles each
    // provider: when `thinking_allowed=false`, no provider sends any thinking
    // field, and the Anthropic / Google / Bedrock servers default to thinking off).
    //
    // - **Auto**: don't pass anything, letting genai go through "model-name-suffix inference" (inside the OpenAI/Anthropic adapter).
    // - **Off + Anthropic / Gemini**: **completely skip `with_reasoning_effort`**, equivalent to
    //   Auto + a model name without a thinking suffix. The genai adapter takes the `(model, None)` inference branch,
    //   doesn't call `insert_anthropic_reasoning` / `thinkingConfig`, and doesn't send a thinking field.
    //   ★ This happens to bypass the vendor genai bug where `claude-opus-4-6` / `claude-sonnet-4-6`
    //   `support_adaptive` force-injects `thinking:{type:adaptive}`
    //   (`lib/rust-genai/src/adapter/adapters/anthropic/adapter_impl.rs:121-135`
    //   doesn't read whether effort is `None`).
    // - **Off + DeepSeek**: the server's `thinking_mode` is enabled by default (deepseek-v4-flash, etc.),
    //   and requires an explicit `extra_body.thinking.type=disabled` to turn off. Zap's local fork
    //   of genai already supports `ChatOptions::extra_body` top-level merging.
    // - **Off + OpenAI / OpenAiResp**: take the `reasoning_effort: "none"` path
    //   (GPT-5 / codex accept the `none` level; o-series is filtered by the capability table).
    // - **non-Off + model doesn't support reasoning**: skip, to avoid injecting thinking parameters into older models like claude-3-5-haiku / gpt-4o /
    //   gemini-1.5-pro and being rejected upstream with a 400.
    use crate::settings::ReasoningEffortSetting as RE;
    match (api_type, effort_setting) {
        // Auto: don't send any parameter
        (_, RE::Auto) => {}

        // Anthropic + Off: don't send a thinking field
        (AgentProviderApiType::Anthropic, RE::Off) => {
            log::info!(
                "[byop] Anthropic Off → skip reasoning_effort (model={model_id}); \
                 no thinking field sent"
            );
        }

        // Gemini + Off: don't send thinkingConfig
        (AgentProviderApiType::Gemini, RE::Off) => {
            log::info!(
                "[byop] Gemini Off → skip reasoning_effort (model={model_id}); \
                 no thinkingConfig sent"
            );
        }

        // DeepSeek + Off: explicitly disabled
        (AgentProviderApiType::DeepSeek, RE::Off) => {
            log::info!(
                "[byop] DeepSeek Off → extra_body thinking.type=disabled (model={model_id})"
            );
            opts = opts.with_extra_body(json!({"thinking": {"type": "disabled"}}));
        }

        // Others (OpenAI / OpenAiResp / Ollama / non-Off levels for each provider):
        // take the reasoning_effort injection path after capability-table filtering
        _ => {
            if let Some(effort) = effort_setting.to_genai() {
                if super::reasoning::model_supports_reasoning(api_type, model_id) {
                    log::info!(
                        "[byop] reasoning_effort injected: model={model_id} setting={effort_setting:?}"
                    );
                    opts = opts.with_reasoning_effort(effort);
                } else {
                    log::info!(
                        "[byop] reasoning_effort SKIPPED: model={model_id} not in capability list \
                         (api_type={api_type:?} setting={effort_setting:?}); request sent without thinking params"
                    );
                }
            }
        }
    }

    // The DashScope (Alibaba Cloud Bailian) OpenAI-compatible path requires an explicit `enable_thinking: true` to
    // output reasoning. See the `dashscope_needs_enable_thinking` comment for details.
    // Mutually exclusive with the DeepSeek Off extra_body above (DeepSeek uses DeepSeek api_type,
    // DashScope uses OpenAI api_type), so they don't fire at the same time.
    if dashscope_needs_enable_thinking(api_type, base_url, model_id, effort_setting) {
        log::info!(
            "[byop] DashScope reasoning model → extra_body enable_thinking=true \
             (model={model_id} setting={effort_setting:?})"
        );
        opts = opts.with_extra_body(json!({"enable_thinking": true}));
    }
    if !extra_headers.is_empty() {
        opts = opts.with_extra_headers(extra_headers);
    }

    opts
}

fn map_genai_error(err: genai::Error) -> OpenAiCompatibleError {
    use genai::Error as G;
    match err {
        // True parse failure: the JSON deserialization stage
        G::StreamParse { .. }
        | G::SerdeJson(_)
        | G::JsonValueExt(_)
        | G::InvalidJsonResponseElement { .. } => OpenAiCompatibleError::Decode(format!("{err}")),

        // Network/streaming-send stage failure (reqwest connection, TLS, DNS, timeout, stream interruption, etc.)
        G::WebStream { .. } | G::WebAdapterCall { .. } | G::WebModelCall { .. } => {
            OpenAiCompatibleError::Stream(format!("{err}"))
        }

        // HTTP error status returned by the server
        G::HttpError {
            status,
            body,
            canonical_reason,
        } => OpenAiCompatibleError::Status {
            status: status.as_u16(),
            body: if canonical_reason.is_empty() {
                body
            } else {
                format!("{canonical_reason}: {body}")
            },
        },

        // Everything else (request construction, authentication, capability unsupported, etc.) is grouped as a generic error, to avoid misleading it as a "parse failure"
        other => OpenAiCompatibleError::Other(format!("{other}")),
    }
}

