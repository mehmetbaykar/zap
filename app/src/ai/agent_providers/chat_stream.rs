    use super::*;
    use genai::chat::{ChatMessage, ChatRole};

    /// Build a typical multi-turn conversation messages sequence:
    /// system + user_1 + assistant_1 + user_2 + assistant_2 + user_3
    /// (ends with user, consistent with `ensure_ends_with_user` output).
    fn build_three_turn_conversation() -> Vec<ChatMessage> {
        vec![
            ChatMessage::system(
                "You are a helpful coding assistant for Zap BYOP.\n\
                 Guidelines: be concise, prefer code over prose.",
            ),
            ChatMessage::user("What is rust borrow checker?"),
            ChatMessage::assistant("It enforces ownership rules at compile time."),
            ChatMessage::user("Show me a code example"),
            ChatMessage::assistant("```rust\nfn main() { let s = String::new(); }\n```"),
            ChatMessage::user("Explain the lifetime in that code"),
        ]
    }

    fn extract_cache_control(msg: &ChatMessage) -> Option<CacheControl> {
        // ChatMessage's cache_control is on `options.cache_control`.
        msg.options.as_ref().and_then(|o| o.cache_control.clone())
    }

    fn cache_signature(msgs: &[ChatMessage]) -> Vec<(usize, ChatRole, Option<CacheControl>)> {
        msgs.iter()
            .enumerate()
            .map(|(i, m)| (i, m.role.clone(), extract_cache_control(m)))
            .collect()
    }

    /// **P0-4 primary acceptance**: repeatedly calling apply_caching_anthropic on the same input
    /// must produce cache mark positions and TTLs that are byte-equal.
    #[test]
    fn apply_caching_anthropic_is_deterministic() {
        let mut a = build_three_turn_conversation();
        let mut b = build_three_turn_conversation();
        apply_caching_anthropic(&mut a);
        apply_caching_anthropic(&mut b);
        assert_eq!(
            cache_signature(&a),
            cache_signature(&b),
            "same input x multiple calls must produce consistent cache marks"
        );
    }

    /// **TTL mixed-strategy acceptance**: system uses 1h (static prefix), non-system uses 5m (session tail).
    /// The order system(1h) → messages(5m) satisfies Anthropic's ordering constraint and is immune to externally injected 5m.
    #[test]
    fn anthropic_cache_uses_mixed_ttl() {
        let mut msgs = build_three_turn_conversation();
        apply_caching_anthropic(&mut msgs);
        let tagged: Vec<_> = msgs
            .iter()
            .filter(|m| extract_cache_control(m).is_some())
            .collect();
        assert!(!tagged.is_empty(), "must set at least one breakpoint");
        for m in &tagged {
            let cc = extract_cache_control(m).unwrap();
            let expected = if matches!(m.role, ChatRole::System) {
                CacheControl::Ephemeral1h
            } else {
                CacheControl::Ephemeral5m
            };
            assert_eq!(
                cc, expected,
                "TTL for role={:?} does not match expected",
                m.role
            );
        }
    }

    /// **P0-4 coverage acceptance**: the opencode approach is first 2 system + last 2 non-system.
    /// A multi-turn conversation (1 system + 5 non-system) should get 1+2=3 marks.
    #[test]
    fn anthropic_marks_first_2_system_and_last_2_non_system() {
        let mut msgs = build_three_turn_conversation();
        apply_caching_anthropic(&mut msgs);
        let tagged_indices: Vec<usize> = msgs
            .iter()
            .enumerate()
            .filter(|(_, m)| extract_cache_control(m).is_some())
            .map(|(i, _)| i)
            .collect();
        // Verify system (idx=0) and the last 2 non-system (idx=4, idx=5) are all marked.
        assert!(tagged_indices.contains(&0), "first system not marked");
        assert!(
            tagged_indices.contains(&4),
            "second-to-last message not marked"
        );
        assert!(tagged_indices.contains(&5), "last message not marked");
        assert_eq!(
            tagged_indices.len(),
            3,
            "3 breakpoints total (1 system + 2 tail)"
        );
    }

    /// **Simulate cache prefix stability across multi-turn conversation**:
    /// turn N's messages are turn N-1's messages + (turn N-1 assistant) + (new user).
    /// The cache marks at the start (system + middle turns) should not drift as the turn count grows.
    #[test]
    fn cache_marks_stable_as_conversation_grows() {
        // turn 1
        let mut t1 = vec![ChatMessage::system("sys"), ChatMessage::user("q1")];
        apply_caching_anthropic(&mut t1);
        let sys_t1_cc = extract_cache_control(&t1[0]);

        // turn 2: add assistant_1 + user_2
        let mut t2 = vec![
            ChatMessage::system("sys"),
            ChatMessage::user("q1"),
            ChatMessage::assistant("a1"),
            ChatMessage::user("q2"),
        ];
        apply_caching_anthropic(&mut t2);
        let sys_t2_cc = extract_cache_control(&t2[0]);

        // The first system's cache_control is consistent across turns → upstream hash unchanged → subsequent hits.
        assert_eq!(
            sys_t1_cc, sys_t2_cc,
            "the first system breakpoint's TTL/position should be consistent across turns"
        );
        // turn 1's user position is marked (tail); turn 2's is no longer marked.
        assert!(extract_cache_control(&t1[1]).is_some());
        assert!(
            extract_cache_control(&t2[1]).is_none(),
            "turn 2's old user is no longer the tail"
        );
    }

    /// **build_chat_options output determinism** (same input yields consistent results across calls).
    /// The minimum bar for a prompt cache hit \u{2014} bit-level hash consistency.
    #[test]
    fn openai_chat_options_is_deterministic() {
        use crate::settings::ReasoningEffortSetting as R;
        let make = || {
            build_chat_options(
                AgentProviderApiType::OpenAi,
                "https://api.openai.com/v1/",
                "gpt-5-mini",
                R::Auto,
                vec![],
                Some("conv-abc-123"),
            )
        };
        let a = make();
        let b = make();
        assert_eq!(a.prompt_cache_key, b.prompt_cache_key);
        assert_eq!(a.cache_control, b.cache_control);
    }

    /// **opencode-compatible whitelisted providers** (api.openai.com / *.openai.azure.com /
    /// openrouter.ai / api.venice.ai / opencode.ai/zen) → emit prompt_cache_key,
    /// and **never emit cache_control** (corresponding to the prompt_cache_retention field;
    /// the opencode repo does not use this field).
    #[test]
    fn whitelisted_provider_emits_prompt_cache_key_only() {
        use crate::settings::ReasoningEffortSetting as R;
        // Pick 5 representative whitelisted URLs, each covering the api_type=OpenAi decision branch.
        let whitelisted = [
            "https://api.openai.com/v1/",
            "https://my-resource.openai.azure.com/openai/v1/",
            "https://openrouter.ai/api/v1/",
            "https://api.venice.ai/api/v1/",
            "https://opencode.ai/zen/v1/",
        ];
        for url in whitelisted {
            let opts = build_chat_options(
                AgentProviderApiType::OpenAi,
                url,
                "gpt-5-mini",
                R::Auto,
                vec![],
                Some("conv-1"),
            );
            assert_eq!(
                opts.prompt_cache_key.as_deref(),
                Some("conv-1"),
                "{url}: whitelisted provider should emit prompt_cache_key=conversation_id"
            );
            assert!(
                opts.cache_control.is_none(),
                "{url}: cache_control is never emitted (opencode does not use prompt_cache_retention)"
            );
        }
    }

    /// **#126 regression**: OpenAi api_type but base_url not on the whitelist (OpenCode Go
