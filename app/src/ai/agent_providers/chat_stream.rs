fn make_user_query_message(
    task_id: &str,
    request_id: &str,
    query: String,
    binaries: &[user_context::UserBinary],
) -> api::Message {
    // Zap: write the multimodal binaries (image / pdf / audio, etc.) into `UserQuery.context.images`
    // (an InputContext field; the proto Image is actually a generic container of `bytes data + string mime_type`,
    // named images for historical reasons). UserBinary.data is a base64 string while proto.data is raw bytes,
    // so decode once here; entries that fail to decode are skipped without blocking the model stream (a decode failure already means this entry
    // wasn't truly sent upstream this turn, so dropping it doesn't affect history consistency).
    let proto_binaries: Vec<api::input_context::Image> = binaries
        .iter()
        .filter_map(|b| {
            use base64::Engine;
            base64::engine::general_purpose::STANDARD
                .decode(&b.data)
                .ok()
                .map(|bytes| api::input_context::Image {
                    data: bytes,
                    mime_type: b.content_type.clone(),
                })
        })
        .collect();
    let context = if proto_binaries.is_empty() {
        None
    } else {
        Some(api::InputContext {
            images: proto_binaries,
            ..Default::default()
        })
    };
    api::Message {
        id: Uuid::new_v4().to_string(),
        task_id: task_id.to_owned(),
        server_message_data: String::new(),
        citations: vec![],
        message: Some(api::message::Message::UserQuery(api::message::UserQuery {
            query,
            context,
            ..Default::default()
        })),
        request_id: request_id.to_owned(),
        timestamp: None,
    }
}

